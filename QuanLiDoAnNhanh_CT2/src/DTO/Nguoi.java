package DTO;

/**
 *
 * @author Vu Xuan Thanh
 */
public class Nguoi {
    protected String hoTen, soDT, diaChi;

    public Nguoi() {
    }

    public Nguoi(String hoTen, String soDT, String diaChi) {
        this.hoTen = hoTen;
        this.soDT = soDT;
        this.diaChi = diaChi;
    }

    public String getHoTen() {
        return hoTen;
    }

    public void setHoTen(String hoTen) {
        this.hoTen = hoTen;
    }

    public String getSoDT() {
        return soDT;
    }

    public void setSoDT(String soDT) {
        this.soDT = soDT;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }
    
    
}
